const STORAGE_KEY = 'palmvellum.landing.lang';
const SUPPORTED = ['en', 'zh-TW', 'zh-CN', 'ja', 'ko', 'ru'];

function detectLang() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && SUPPORTED.includes(stored)) return stored;
  } catch (_) {}
  const bl = (navigator.language || 'en').toLowerCase();
  if (bl.startsWith('zh-tw') || bl.startsWith('zh-hk') || bl.startsWith('zh-hant')) return 'zh-TW';
  if (bl.startsWith('zh')) return 'zh-CN';
  if (bl.startsWith('ja')) return 'ja';
  if (bl.startsWith('ko')) return 'ko';
  if (bl.startsWith('ru')) return 'ru';
  return 'en';
}

function applyLang(lang) {
  if (!SUPPORTED.includes(lang)) lang = 'en';
  const dict = DICT[lang] || DICT.en;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const val = dict[key] ?? DICT.en[key];
    if (val == null) return;
    if (el.tagName === 'META') {
      el.setAttribute('content', val.replace(/<[^>]+>/g, ''));
    } else {
      el.innerHTML = val;
    }
  });
  try { localStorage.setItem(STORAGE_KEY, lang); } catch (_) {}
  const picker = document.getElementById('lang-picker');
  if (picker && picker.value !== lang) picker.value = lang;
}

document.addEventListener('DOMContentLoaded', () => {
  const initial = detectLang();
  applyLang(initial);
  const picker = document.getElementById('lang-picker');
  if (picker) {
    picker.addEventListener('change', (e) => applyLang(e.target.value));
  }
});
